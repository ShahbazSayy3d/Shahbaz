<# WMI = Windows Management Instrumentation
Windows Management Instrumentation consists of a set of extensions 
to the Windows Driver Model that provides an operating system interface 
through which instrumented components provide information and notification. 
#>

<# CIM = Common Information Model
Common Information Model (CIM) session is a client-side object 
that represents a connection to a local computer or a remote computer.
Microsoft has since deprecated the WMI commands in Windows in favor of their CIM counterparts.
#>